# operasi matematika

a = 10
b = 3

# operasi penjumlahan
hasil = a + b
print(a, '+', b, '=', hasil)

# operasi pengurangan
hasil = a - b
print(a, '-', b, '=', hasil)

# operasi perkalian
hasil = a * b
print(a, '*', b, '=', hasil)

# operasi pembagian
hasil = a / b
print(a, '/', b, '=', hasil)

# operasi exponen(pangkat)
hasil = a ** b 
print(a, '**', b, '=', hasil)

# operasi modulus
hasil = a % b
print(a, '%', b, '=', hasil)

# operasi floor division(pembulatan kebawah)
hasil = a // b
print(a, '//', b, '=', hasil)