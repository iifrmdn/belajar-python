data_string = str(data_integer)
data_boolean = bool(data_integer)
print("Data : ", data_float)
print("- Bertipe : ", type(data_float), "\n")
print("Data : ", data_string)
print("- Bertipe : ", type(data_string), "\n")
print("Data : ", data_boolean)
print("- Bertipe : ", type(data_boolean), "\n")
