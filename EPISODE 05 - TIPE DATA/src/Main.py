# tipe data integer
data_integer = 10
print("Data : ", data_integer)
print("- Bertipe : ", type(data_integer), "\n")

# tipe data float
data_float = 1.5
print("Data : ", data_float)
print("- Bertipe : ", type(data_float), "\n")

# tipe data string
data_string = "Iif Ramadhan"
print("Data : ", data_string)
print("- Bertipe : ", type(data_string), "\n")

# tipe data boolean
data_bool = True
print("Data : ", data_bool)
print("- Bertipe : ", type(data_bool), "\n")

# tipe data dari bahasa C
from ctypes import c_double
data_double = c_double(10.5)
print("Data : ", data_double)
print("- Bertipe : ", type(data_double), "\n")









