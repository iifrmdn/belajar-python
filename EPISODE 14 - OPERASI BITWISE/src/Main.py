# Operasi Bitwise
a = 1
b = 2

# AND
print("==AND==")
c = a & b
print("nilai :", a, "binary :",format(a,'08b'))
print("nilai :", b, "binary :",format(b,'08b'))
print("c = a & b")
print("nilai :",c, "binary :",format(c, '08b'))

# OR
print("==OR==")
c = a | b
print("nilai :", a, "binary :",format(a,'08b'))
print("nilai :", b, "binary :",format(b,'08b'))
print("c = a | b")
print("nilai :",c, "binary :",format(c, '08b'))

# XOR
print("==XOR==")
c = a ^ b
print("nilai :", a, "binary :",format(a,'08b'))
print("nilai :", b, "binary :",format(b,'08b'))
print("c = a ^ b")
print("nilai :",c, "binary :",format(c, '08b'))