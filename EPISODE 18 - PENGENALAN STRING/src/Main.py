# membuat string

# 1. menggunakan single quote
print('kalimat ini dibuat menggunakan single quote')

# 2. menggunakan double quote
print("kalimat ini dibuat menggunakan double quote")

# 3. membuat tanda ' menjadi string menggunakan \
print('Sekarang adalah hari jum\'at')

# 4. membuat tanda \ menjadi string menggunakan \\
print("C:\\User\\Iif Ramadhan")

# 5. membuat tab menggunakan \t
print("Hello \tWorld")

# 6. membuat backspace menggunakan \b
print("Hello \bWorld")

# 7. membuat new line menggunakan \n
print("Hello \nWorld")

# 8. membuat raw string
print(r"\new folder \b \t \n \\ tidak akan di anggap")

# 9. multiline literal string
print("""
Nama : Arif Nur Ramadhan
Jurusan : Sistem Informasi
"""
)

# 10. gabungan raw dan multiline literal string
print("""
Nama : Arif Nur Ramadhan
Jurusan : Sistem Informasi
Website : www.bssn.go.id/pejabat/arifnurramadhan
"""
)